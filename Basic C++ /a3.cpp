#include <iostream>
using namespace std;

int main()
{
    int a, b;

    cout << "Enter 1st number " << endl;
    cin >> a;

    cout << "Enter 2nd number " << endl;
    cin >> b;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << (float)a / b << endl;

    return 0;
}